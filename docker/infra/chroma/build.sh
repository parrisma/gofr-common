#!/bin/bash
# =======================================================================
# ChromaDB Build Script
# =======================================================================

set -e

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"

echo "======================================================================="
echo "Building GOFR ChromaDB Image"
echo "======================================================================="

docker build \
    -f "${SCRIPT_DIR}/Dockerfile" \
    -t gofr-chroma:latest \
    "${SCRIPT_DIR}"

echo ""
echo "======================================================================="
echo "Build complete: gofr-chroma:latest"
echo "======================================================================="
docker images gofr-chroma --format "  {{.Repository}}:{{.Tag}} ({{.Size}})"
echo "======================================================================="
