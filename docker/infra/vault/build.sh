#!/bin/bash
# =======================================================================
# HashiCorp Vault Build Script
# =======================================================================

set -e

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"

echo "======================================================================="
echo "Building GOFR Vault Image"
echo "======================================================================="

docker build \
    -f "${SCRIPT_DIR}/Dockerfile" \
    -t gofr-vault:latest \
    "${SCRIPT_DIR}"

echo ""
echo "======================================================================="
echo "Build complete: gofr-vault:latest"
echo "======================================================================="
docker images gofr-vault --format "  {{.Repository}}:{{.Tag}} ({{.Size}})"
echo "======================================================================="
