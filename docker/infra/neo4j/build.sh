#!/bin/bash
# =======================================================================
# Neo4j Build Script
# =======================================================================

set -e

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"

echo "======================================================================="
echo "Building GOFR Neo4j Image"
echo "======================================================================="

docker build \
    -f "${SCRIPT_DIR}/Dockerfile" \
    -t gofr-neo4j:latest \
    "${SCRIPT_DIR}"

echo ""
echo "======================================================================="
echo "Build complete: gofr-neo4j:latest"
echo "======================================================================="
docker images gofr-neo4j --format "  {{.Repository}}:{{.Tag}} ({{.Size}})"
echo "======================================================================="
